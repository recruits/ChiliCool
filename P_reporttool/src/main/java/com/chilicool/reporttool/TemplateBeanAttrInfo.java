package com.chilicool.reporttool;

public class TemplateBeanAttrInfo {
	private String attrType;
	private String attrName;
	private String attrMethodName;
	private String attrBeanType;
	private boolean isList;
	public String getAttrType() {
		return attrType;
	}
	public void setAttrType(String attrType) {
		this.attrType = attrType;
	}
	public String getAttrName() {
		return attrName;
	}
	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}
	public String getAttrMethodName() {
		return attrMethodName;
	}
	public void setAttrMethodName(String attrMethodName) {
		this.attrMethodName = attrMethodName;
	}
	public String getAttrBeanType() {
		return attrBeanType;
	}
	public void setAttrBeanType(String attrBeanType) {
		this.attrBeanType = attrBeanType;
	}
	public boolean isList() {
		return isList;
	}
	public void setList(boolean isList) {
		this.isList = isList;
	}
}
